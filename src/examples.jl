#example for ramified complex

function star(n::Int)

    vertices=[[0.0,0.0,0.5],[0.0,0.0,-0.5]]

    facets=[]
    edges=[[1,2]]
    for i in 1:n
	push!(edges,[1,i+2])
	push!(edges,[2,i+2])
	push!(facets,[1,2,i+2])
      push!(vertices,[sqrt(3.0)/2.0*cos(2*3.1415/n),sqrt(3.0)/2*cos(2.0*3.1415/n),0.0])
    end
   return Polyhedron(vertices,edges,facets) 
end
